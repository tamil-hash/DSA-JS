/**
 * 1. Understand the problem.
 * * Can i restate the problems in my own words.
 * * What are the inputs that goes into the problem?
 * * What are the output that should come from the solution to the problem?
 * * Can the inputs be determined from the inputs? In other words, do I have enough Information to solve the problem?
 * * How should i label the important pieces of data that are part of the problem.
 */

/**
 * 2. Explore Concrete examples.
 * * Start with simple examples.
 * * Progress to more complex examples.
 * * Explore examples with invalid inputs.
 */

/**
 * 3. Break it down.
 * * Explicitly write out the steps you need to take.
 */

/**
 * 4. Solve/Simplify.
 * * Solve the problem, if you can't solve the simple problem.
 * * Find the core difficulty in what you're trying to do.
 * * Temporarily ignore that difficulty.
 * * Write a simplified solution.
 * * Then incorporate that difficulty back in.
 */

/**
 * 5. Look back and refactor.
 * *
 * * Refactoring Questions.
 * * * Can you check the results?
 * * * Can you derive the result differently?
 * * * Can you understand it at a glance?
 * * * Can you use the result or method for some other problem?
 * * * Can you improve the performance of your solution?
 * * * Can you think of other ways to refactor?
 * * * How have other people solved this problem?
 */
